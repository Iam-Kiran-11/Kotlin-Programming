fun main() {
    val a: Int = 34  // Entering a value in val variable type
    println(a)      // printing the val variable type

    // This value can't be changed after declaration

    var b: Float = 1.45f  //Entering a value in var variable type
    println(b)           // Printing the var variable type
    
    //This value can be changed

    b = 2.45f // Changing the value of b
    println(b)
    
}