fun main() {
    val byteType: Byte = -12     //Byte data type is defined as the smallest integer type
    val shortType: Short = 3276  //Short data type is defined as short inetger
    val longType: Long = 123456789L //Long data type is defined as long interger and use of 'L' as suffix 
    val intType: Int = 23456        //Interger type
    val floatType: Float = 1.453f   // Float data type and use of 'f' as suffix
    val doubleType: Double = 6.12345 //Double data type
    val charType: Char = 'B'         //Character 
    val stringType: String = "Developer's World" //String
    val booleanType: Boolean = false //True or false

// Printing all the data types 

    println(byteType)
    println(shortType)
    println(longType)
    println(intType)
    println(floatType)
    println(doubleType)
    println(charType)
    println(stringType)
    println(booleanType)
}